public class Animal {
    private String name, view, color;
    public Animal (String name, String view, String color) {
        this.name = name;
        this.view = view;
        this.color = color;
            }
            public String getName () {
        return name;
            }
            public String getView() {
        return view;
            }
            public String getColor () {
        return color;
            }
}
